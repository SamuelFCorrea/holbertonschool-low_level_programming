#include "3-calc.h"

/**
 * op_add - calculate the addition
 * @a: int
 * @b: int
 *
 * Return: addition result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate the diference
 * @a: int
 * @b: int
 *
 * Return: diference result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate the product
 * @a: int
 * @b: int
 *
 * Return: product result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate the diference
 * @a: int
 * @b: int
 *
 * Return: deference result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the diference
 * @a: int
 * @b: int
 *
 * Return: module result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
